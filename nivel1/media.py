# Exibir a media "simples", "harmônica" e "ponderada" entre dois números
n1 = 26
n2 = 25
p1 = 1
p2 = 1
media_simples = (n1 + n2) / 2
media_harmonica = 2 / ((1/n1) + (1/n2))
media_ponderada = ((n1 * p1) + (n2 * p2)) / (p1 + p2)

print(media_simples)
print(media_harmonica)
print(media_ponderada)
